a = input('Digite algo: ')
print('O tipo primitivo desse valor é:\033[1;30m{}\033[m'.format(type(a)))
print('É numérico? \033[1;31m{}\033[m'.format(a.isnumeric()))
print('É decimal? \033[1;32m{}\033[m'.format(a.isdecimal()))
print('É alfabético? \033[1;33m{}\033[m'.format(a.isalpha()))
print('É alfanumérico? \033[1;34m{}\033[m'.format(a.isalnum()))
print('Está em maiúsculas? \033[1;35m{}\033[m'.format(a.islower()))
print('Está em minúsculas: \033[1;36m{}\033[m'.format(a.isupper()))
print('Está capitalizado? \033[1;37m{}\033[m'.format(a.istitle()))
